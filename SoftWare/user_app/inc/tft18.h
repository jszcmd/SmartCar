/*********************************************************************************************************************
 * 文件名称：tft18.h
 * 功能描述：TFT18 屏幕显示封装头文件
 *
 * ─────────────────────────────────────────────────────────────────────
 * 屏幕参数：
 *   分辨率：竖屏 162 × 132（默认），横屏 132 × 162
 *   字体：  6×8（小）、8×8（中，默认）、8×16（大）
 *
 * ─────────────────────────────────────────────────────────────────────
 * 使用注意：
 *   - 所有"绘制"函数只写入缓冲区，最后必须调 tft18_flush() 推屏
 *   - 颜色用 lq_display 库的 U16XXXX 宏（U16RED / U16GREEN / U16BLACK）
 *   - 坐标 (x, y) 单位是像素，左上角为 (0, 0)
 *
 * ─────────────────────────────────────────────────────────────────────
 * 三种调用方式：
 *
 *   方式 A：手动定位（原版本）
 *     tft18_int16(0, 0,  gz);
 *     tft18_int16(0, 16, gx);
 *
 *   方式 B：标签 + 数值
 *     tft18_label_int16(0, 0,  "gz", gz);
 *
 *   方式 C：自动换行（新增，最方便）
 *     tft18_reset_cursor();
 *     tft18_println("State: %s", state_str);
 *     tft18_println("Speed: %.2f", speed);
 *     // 自动一行一行往下排
 *
 * ─────────────────────────────────────────────────────────────────────
 * 高级控件：
 *   tft18_progress_bar(x, y, w, h, percent)  → 进度条
 *   tft18_h_bar(x, y, w, h, value, max)      → 横向柱状条
 *   tft18_status_led(x, y, status)           → 彩色状态灯
 *   tft18_rect / tft18_line / tft18_circle    → 绘图原语
 ********************************************************************************************************************/

#ifndef TFT18_H
#define TFT18_H

#include "lq_display_types.hpp"
#include "lq_display_tft18.hpp"
#include "lq_display_font.hpp"
#include <stdint.h>

// ══════════════════════════════════════════════════════════════
// 屏幕方向配置（1=竖屏，0=横屏）
// ══════════════════════════════════════════════════════════════
#define TFT18_PORTRAIT 1

// ⚠️ 注意：lq_display_tft18.hpp 已经定义了 TFT18_WIDTH / TFT18_HEIGHT
//          所以这里用带前缀的名字，避免重复定义警告
#if TFT18_PORTRAIT
#define TFT18_SCREEN_W 162
#define TFT18_SCREEN_H 132
#else
#define TFT18_SCREEN_W 132
#define TFT18_SCREEN_H 162
#endif

// ══════════════════════════════════════════════════════════════
// 默认配色
// ══════════════════════════════════════════════════════════════
#define TFT18_DEFAULT_FG U16BLACK // 文字颜色
#define TFT18_DEFAULT_BG U16GREEN // 背景色

// ══════════════════════════════════════════════════════════════
// 字体大小枚举
// ══════════════════════════════════════════════════════════════
typedef enum
{
    TFT_FONT_6X8 = 0, // 小字号（最多每行 27 字符）
    TFT_FONT_8X8 = 1, // 中字号（默认，每行 20 字符）
    TFT_FONT_8X16 = 2 // 大字号（每行 20 字符，但行高 16）
} TftFont_t;

// ══════════════════════════════════════════════════════════════
// 状态灯状态
// ══════════════════════════════════════════════════════════════
typedef enum
{
    TFT_LED_OFF = 0,   // 灰色（关闭）
    TFT_LED_OK = 1,    // 绿色（正常）
    TFT_LED_WARN = 2,  // 黄色（警告）
    TFT_LED_ERROR = 3, // 红色（错误）
    TFT_LED_INFO = 4   // 蓝色（信息）
} TftLedState_t;

// ══════════════════════════════════════════════════════════════
// 函数声明 - 基础接口（向下兼容原版本）
// ══════════════════════════════════════════════════════════════

void tft18_init(void);
void tft18_flush(void);
void tft18_cls(lq_display_color_t color = U16GREEN);

// 整数显示
void tft18_uint8(uint8_t x, uint8_t y, uint8_t val);
void tft18_uint16(uint8_t x, uint8_t y, uint16_t val);
void tft18_uint32(uint8_t x, uint8_t y, uint32_t val);
void tft18_uint64(uint8_t x, uint8_t y, uint64_t val);
void tft18_int8(uint8_t x, uint8_t y, int8_t val);
void tft18_int16(uint8_t x, uint8_t y, int16_t val);
void tft18_int32(uint8_t x, uint8_t y, int32_t val);
void tft18_int64(uint8_t x, uint8_t y, int64_t val);

// 浮点和字符串
void tft18_float(uint8_t x, uint8_t y, float val, uint8_t decimals = 2);
void tft18_string(uint8_t x, uint8_t y, const char *str);

// 标签 + 数值
void tft18_label_int16(uint8_t x, uint8_t y, const char *label, int16_t val);
void tft18_label_uint16(uint8_t x, uint8_t y, const char *label, uint16_t val);
void tft18_label_float(uint8_t x, uint8_t y, const char *label, float val, uint8_t decimals = 2);

// ══════════════════════════════════════════════════════════════
// 函数声明 - 多字号
// ══════════════════════════════════════════════════════════════

/*******************************************************************
 * @brief   设置当前字体（影响后续 tft18_string 等调用）
 ******************************************************************/
void tft18_set_font(TftFont_t font);

/*******************************************************************
 * @brief   获取当前字体的字符宽高（用于布局计算）
 ******************************************************************/
uint8_t tft18_font_width(void);
uint8_t tft18_font_height(void);

/*******************************************************************
 * @brief   设置默认前景/背景色
 ******************************************************************/
void tft18_set_color(lq_display_color_t fg, lq_display_color_t bg);

// ══════════════════════════════════════════════════════════════
// 函数声明 - 自动换行（推荐使用）
// ══════════════════════════════════════════════════════════════

/*******************************************************************
 * @brief   重置文字光标到屏幕左上角
 *          通常每次刷新前调用一次
 ******************************************************************/
void tft18_reset_cursor(void);

/*******************************************************************
 * @brief   打印一行（自动换行，类似 printf）
 *          每次调用 y 坐标自动 += 字体高度
 *          超出屏幕底部会回到顶部（环绕）
 *
 * @example
 *     tft18_reset_cursor();
 *     tft18_println("State: %s", "RUNNING");
 *     tft18_println("Speed: %.2f m/s", 1.23);
 *     tft18_println("Battery: %d%%", 85);
 *     tft18_flush();
 ******************************************************************/
void tft18_println(const char *fmt, ...);

// ══════════════════════════════════════════════════════════════
// 函数声明 - 绘图原语
// ══════════════════════════════════════════════════════════════

/*******************************************************************
 * @brief   画点
 ******************************************************************/
void tft18_pixel(uint8_t x, uint8_t y, lq_display_color_t color);

/*******************************************************************
 * @brief   画水平/垂直线
 ******************************************************************/
void tft18_hline(uint8_t x, uint8_t y, uint8_t w, lq_display_color_t color);
void tft18_vline(uint8_t x, uint8_t y, uint8_t h, lq_display_color_t color);

/*******************************************************************
 * @brief   画矩形（边框）
 ******************************************************************/
void tft18_rect(uint8_t x, uint8_t y, uint8_t w, uint8_t h, lq_display_color_t color);

/*******************************************************************
 * @brief   填充矩形（实心）
 ******************************************************************/
void tft18_rect_filled(uint8_t x, uint8_t y, uint8_t w, uint8_t h, lq_display_color_t color);

// ══════════════════════════════════════════════════════════════
// 函数声明 - 高级控件
// ══════════════════════════════════════════════════════════════

/*******************************************************************
 * @brief   绘制进度条
 * @param   x, y     左上角
 * @param   w, h     宽高
 * @param   percent  进度百分比（0~100）
 *
 * @example
 *     tft18_progress_bar(10, 50, 100, 12, 75);  // 75% 进度条
 ******************************************************************/
void tft18_progress_bar(uint8_t x, uint8_t y,
                        uint8_t w, uint8_t h,
                        uint8_t percent);

/*******************************************************************
 * @brief   绘制水平柱状条（用于显示当前值/最大值的比例）
 * @param   value/max_value  当前值和最大值
 *
 * @example
 *     tft18_h_bar(0, 60, 120, 8, encoder_L_final, 100.0f);
 ******************************************************************/
void tft18_h_bar(uint8_t x, uint8_t y,
                 uint8_t w, uint8_t h,
                 float value, float max_value);

/*******************************************************************
 * @brief   绘制状态指示灯（彩色小圆，5×5 像素）
 ******************************************************************/
void tft18_status_led(uint8_t x, uint8_t y, TftLedState_t status);

/*******************************************************************
 * @brief   绘制带标签的状态灯
 *          格式：[●] label
 ******************************************************************/
void tft18_status_led_label(uint8_t x, uint8_t y,
                            const char *label,
                            TftLedState_t status);

#endif // TFT18_H