/*********************************************************************************************************************
 * 文件名称：img_process.h
 * 功能描述：摄像头初始化、图传管理、图像处理与中线误差计算 — 头文件
 ********************************************************************************************************************/

#ifndef __IMG_PROCESS_H_
#define __IMG_PROCESS_H_

#include "headfile.h"
#include "WW_CAMERA.h"
#include "WW_transmission.h"

// ══════════════════════════════════════════════════════════════
// 全局指针声明
// 定义在 img_process.cpp，其他文件通过 extern 使用
// ══════════════════════════════════════════════════════════════
extern Camera *g_camera;
extern TransmissionStreamServer *g_server;

// ══════════════════════════════════════════════════════════════
// 全局图像变量声明
// 定义在 img_process.cpp，其他文件通过 extern 使用
// ══════════════════════════════════════════════════════════════
extern cv::Mat img;          // 摄像头原始彩色图像
extern cv::Mat img_gray;     // 灰度图像
extern cv::Mat black_img;    // 纯黑背景图（用于绘制辅助线）
extern cv::Mat detect;       // resize后的检测图像
extern cv::Mat img_binary;   // 二值化图像
extern cv::Mat img_nitoushi; // 逆透视图像（由 image_handle 更新）

// ══════════════════════════════════════════════════════════════
// 函数声明
// ══════════════════════════════════════════════════════════════

/*******************************************************************
 * @brief   摄像头 + 图传初始化
 *          程序启动时调用一次
 * @param   width   摄像头宽度
 * @param   height  摄像头高度
 * @param   fps     帧率
 * @return  true=成功  false=失败
 ******************************************************************/
bool camera_server_init(int width, int height, int fps);

/*******************************************************************
 * @brief   释放摄像头和图传资源
 *          程序退出时调用
 ******************************************************************/
void camera_server_deinit();

/*******************************************************************
 * @brief   零拷贝图传
 *          直接推送JPEG原始数据，性能最高，适合传原图
 ******************************************************************/
void camera_transmit_raw();

/*******************************************************************
 * @brief   普通图传
 *          传入已处理的 Mat 图像（如逆透视图、带辅助线的图）
 * @param   frame   要推送的图像
 ******************************************************************/
void camera_transmit(const cv::Mat &frame);

/*******************************************************************
 * @brief   获取中线误差
 *          每个控制周期调用一次，内部处理一帧后立即返回
 *          注意：内部不含死循环，循环由调用方负责
 * @return  中线偏差值 Guide
 ******************************************************************/
float get_errors();

#endif // __IMG_PROCESS_H_