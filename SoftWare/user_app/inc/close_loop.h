/*********************************************************************************************************************
 * 文件名称：close_loop.h
 * 功能描述：电机闭环控制头文件
 *
 * ─────────────────────────────────────────────────────────────────────
 * 控制框图：
 *
 *         ┌──────────────────────────────────────────┐
 *         │                                          │
 *   target─────►[误差]──►[死区]──►[增量PID]──►[限幅]──┼──►Set_Motor_PWM
 *                  ▲                                  │
 *                  │                                  │
 *              encoder_final ◄────────────────────────┘
 *                  │
 *                  └──►[堵转检测] ──► 紧急停车
 *
 * ─────────────────────────────────────────────────────────────────────
 * 状态机：
 *   IDLE        → 空闲（未设定目标，电机停止）
 *   RUNNING     → 正常运行中
 *   STALLED     → 检测到电机堵转，已紧急停车
 *   EMERGENCY   → 用户主动调用紧急停车
 *
 *   状态转移：
 *     IDLE      ──set_target──►  RUNNING
 *     RUNNING   ──stop──────►   IDLE
 *     RUNNING   ──检测堵转──►   STALLED
 *     RUNNING   ──em_stop───►   EMERGENCY
 *     STALLED   ──reset────►   IDLE
 *     EMERGENCY ──reset────►   IDLE
 *
 * ─────────────────────────────────────────────────────────────────────
 * 调用顺序：
 *   1. close_loop_init()                    → main 里初始化
 *   2. close_loop_set_target(L, R)          → 设速度（直接指定左右）
 *      或 close_loop_set_target_diff(B, D)  → 设速度（base+diff差速）
 *   3. close_loop_update()                  → 定时器周期调用
 *   4. close_loop_stop()                    → 正常停止
 *   5. close_loop_emergency_stop()          → 紧急停车（硬刹）
 ********************************************************************************************************************/

#ifndef CLOSE_LOOP_H
#define CLOSE_LOOP_H

#include "pid.h"
#include "motor.h"
#include <stdio.h>
#include <atomic>
#include <stdint.h>

// ══════════════════════════════════════════════════════════════
// 配置宏（根据实际调试调整）
// ══════════════════════════════════════════════════════════════

// PID 默认参数
#define CL_DEFAULT_KP 220.0f
#define CL_DEFAULT_KI 4.0f
#define CL_DEFAULT_KD 0.0f

// 死区阈值：|误差| < 此值时不调整 PID 输出，防止低速抖动
// 设为 0 = 关闭死区
#define CL_ERROR_DEADZONE 0.5f

// 堵转检测参数
// 条件：连续 N 次满足 (PID输出高 但 编码器反馈低)
#define CL_STALL_PWM_THRESH 8000      // PID 输出高于此值
#define CL_STALL_FEEDBACK_THRESH 1.0f // 同时编码器反馈低于此值
#define CL_STALL_COUNT_THRESH 20      // 连续 20 次（5ms × 20 = 100ms）→ 判定堵转
#define CL_STALL_DETECT_ENABLE 1      // 1=启用，0=禁用

// PID 输出限幅（匹配 PWM_MAX）
#define CL_PID_OUT_MIN 0.0f
#define CL_PID_OUT_MAX 9900.0f // 与 PWM_MAX 一致

// ══════════════════════════════════════════════════════════════
// 闭环状态枚举
// ══════════════════════════════════════════════════════════════
typedef enum
{
    CLOSE_LOOP_IDLE = 0,     // 空闲（无目标）
    CLOSE_LOOP_RUNNING = 1,  // 运行中
    CLOSE_LOOP_STALLED = 2,  // 检测到堵转
    CLOSE_LOOP_EMERGENCY = 3 // 紧急停车中
} CloseLoopState_t;

// ══════════════════════════════════════════════════════════════
// 外部变量声明
// ══════════════════════════════════════════════════════════════

// 系统运行标志（main.cpp 定义）
extern std::atomic<bool> ls_system_running;

// PID 控制器（pid.h 已声明，这里再 extern 一次方便阅读）
extern PID_TypeDef LSpeed_PID;
extern PID_TypeDef RSpeed_PID;

// 编码器反馈（encoder.h 已定义）
extern float encoder_L_final;
extern float encoder_R_final;

// ══════════════════════════════════════════════════════════════
// 函数声明 - 基础接口
// ══════════════════════════════════════════════════════════════

/*******************************************************************
 * @brief   闭环控制初始化
 *          配置 PID 参数和限幅，重置目标速度，停止电机
 *          在 main() 里调用一次
 ******************************************************************/
void close_loop_init(void);

/*******************************************************************
 * @brief   设置目标速度（直接指定左右）
 *          内部自动限幅到 [0, PWM_MAX]
 * @param   left_speed   左轮目标
 * @param   right_speed  右轮目标
 ******************************************************************/
void close_loop_set_target(float left_speed, float right_speed);

/*******************************************************************
 * @brief   设置目标速度（差速形式，更直观）
 *          左轮 = base - diff
 *          右轮 = base + diff
 * @param   base   基础速度
 * @param   diff   差速值（正→左转，负→右转）
 ******************************************************************/
void close_loop_set_target_diff(float base, float diff);

/*******************************************************************
 * @brief   闭环控制更新（定时器周期调用）
 *          - 读编码器反馈
 *          - 计算误差，应用死区
 *          - 调用 PID
 *          - 输出 PWM
 *          - 检测堵转
 ******************************************************************/
void close_loop_update(void);

/*******************************************************************
 * @brief   正常停止（清零目标 + Reset PID + 滑行）
 *          状态变为 IDLE
 ******************************************************************/
void close_loop_stop(void);

/*******************************************************************
 * @brief   紧急停车（清零目标 + Reset PID + 硬刹车）
 *          状态变为 EMERGENCY
 *          需要调用 close_loop_resume() 才能重新工作
 ******************************************************************/
void close_loop_emergency_stop(void);

/*******************************************************************
 * @brief   从异常状态（STALLED / EMERGENCY）恢复到 IDLE
 *          清空所有状态后允许重新启动
 ******************************************************************/
void close_loop_resume(void);

// ══════════════════════════════════════════════════════════════
// 函数声明 - 查询接口
// ══════════════════════════════════════════════════════════════

/*******************************************************************
 * @brief   获取左/右电机当前目标速度
 ******************************************************************/
float close_loop_get_left_target(void);
float close_loop_get_right_target(void);

/*******************************************************************
 * @brief   获取闭环当前状态
 ******************************************************************/
CloseLoopState_t close_loop_get_state(void);

/*******************************************************************
 * @brief   状态字符串（用于打印/屏幕显示）
 ******************************************************************/
const char *close_loop_state_str(void);

// ══════════════════════════════════════════════════════════════
// 函数声明 - PID 参数动态调整（不用改源码即可调参）
// ══════════════════════════════════════════════════════════════

/*******************************************************************
 * @brief   动态修改左右 PID 的 Kp/Ki/Kd
 *          调试时可绑定到按键或上位机命令
 ******************************************************************/
void close_loop_set_pid(float kp, float ki, float kd);

#endif // CLOSE_LOOP_H