/*********************************************************************************************************************
 * 文件名称：distance_sensor.h
 * 功能描述：VL53L0X 激光测距传感器封装头文件
 *
 * ─────────────────────────────────────────────────────────────────────
 * 数据处理流程：
 *
 *   硬件原始值 (uint16_t mm)
 *           │
 *           ▼
 *      有效性检查        ← 超量程(>=8190) → 标记 invalid
 *           │
 *           ▼
 *   写入环形缓冲区 (大小=DIST_FILTER_WINDOW)
 *           │
 *           ▼
 *   中位数滤波 + 均值滤波 → g_distance（去除尖刺，再平均）
 *           │
 *           ▼
 *      区间分类 → g_distance_zone (CRITICAL/NEAR/MID/FAR)
 *           │
 *           ▼
 *      变化率检测 → 检测到快速接近 → 触发用户回调
 *
 * ─────────────────────────────────────────────────────────────────────
 * 距离区间划分（默认值，根据场景调整）：
 *
 *   distance < 50mm    → CRITICAL  即将碰撞，必须立即停车
 *   distance < 100mm   → NEAR      很近，建议减速
 *   distance < 200mm   → MID       有障碍，可避让
 *   distance < 500mm   → FAR       距离合适
 *   distance >= 500mm  → SAFE      远距离
 *   distance == INVALID → 数据无效（超量程/读取失败）
 *
 * ─────────────────────────────────────────────────────────────────────
 * 使用示例：
 *
 *   // 1. 基本读取
 *   uint16_t d = distance_get();
 *
 *   // 2. 区间判断
 *   if (distance_get_zone() == DIST_ZONE_CRITICAL) close_loop_emergency_stop();
 *
 *   // 3. 注册临界回调
 *   distance_register_critical_cb(my_emergency_handler);
 *
 *   // 4. 变化率检测
 *   if (distance_is_approaching_fast()) close_loop_set_target(1000, 1000);
 ********************************************************************************************************************/

#ifndef DISTANCE_SENSOR_H
#define DISTANCE_SENSOR_H

#include <lq_i2c_vl53l0x.hpp>
#include <stdint.h>

// ══════════════════════════════════════════════════════════════
// 配置宏（根据场景调整）
// ══════════════════════════════════════════════════════════════

// 距离区间阈值（单位：mm）
#define DIST_CRITICAL_THRESHOLD 50 // 临界：必须急停
#define DIST_NEAR_THRESHOLD 100    // 近：建议减速
#define DIST_MID_THRESHOLD 200     // 中：有障碍
#define DIST_FAR_THRESHOLD 500     // 远：距离合适

// 兼容旧代码的别名（保留旧的命名）
#define DIST_OBSTACLE_THRESHOLD DIST_MID_THRESHOLD
#define DIST_INVALID 8190 // VL53L0X 超量程值

// 滤波窗口长度（取中位数 + 均值）
// 5 表示去掉最大最小，剩 3 个求平均
#define DIST_FILTER_WINDOW 5

// 变化率检测阈值
// 在 DIST_APPROACH_TICKS 个周期内，距离变化超过 DIST_APPROACH_DELTA → 视为快速接近
#define DIST_APPROACH_DELTA 100 // mm
#define DIST_APPROACH_TICKS 10  // 周期数（5ms × 10 = 50ms）

// 连续读取失败次数阈值（超过则标记设备故障）
#define DIST_FAIL_THRESHOLD 50

// ══════════════════════════════════════════════════════════════
// 距离区间枚举
// ══════════════════════════════════════════════════════════════
typedef enum
{
    DIST_ZONE_CRITICAL = 0, // < 50mm  即将碰撞
    DIST_ZONE_NEAR = 1,     // < 100mm 很近
    DIST_ZONE_MID = 2,      // < 200mm 中等
    DIST_ZONE_FAR = 3,      // < 500mm 较远
    DIST_ZONE_SAFE = 4,     // >= 500mm 安全
    DIST_ZONE_INVALID = 5   // 数据无效
} DistZone_t;

// 临界回调函数类型
typedef void (*dist_critical_cb_t)(uint16_t distance_mm);

// ══════════════════════════════════════════════════════════════
// 全局变量声明
// ══════════════════════════════════════════════════════════════
extern uint16_t g_distance;        // 滤波后的距离（mm）
extern uint16_t g_distance_raw;    // 原始距离（mm）
extern bool g_distance_valid;      // 数据是否有效
extern DistZone_t g_distance_zone; // 当前区间

// ══════════════════════════════════════════════════════════════
// 函数声明 - 基础接口
// ══════════════════════════════════════════════════════════════

/*******************************************************************
 * @brief   测距数据更新（定时器周期调用）
 *          流程：读硬件 → 滤波 → 区间分类 → 变化率检测 → 触发回调
 ******************************************************************/
void distance_sensor_update(void);

/*******************************************************************
 * @brief   打印当前测距数据（调试用）
 ******************************************************************/
void distance_sensor_print(void);

/*******************************************************************
 * @brief   获取当前测距值（带有效性检查）
 * @return  有效时返回 g_distance，否则返回 DIST_INVALID
 ******************************************************************/
uint16_t distance_get(void);

/*******************************************************************
 * @brief   获取原始未滤波距离值
 ******************************************************************/
uint16_t distance_get_raw(void);

// ══════════════════════════════════════════════════════════════
// 函数声明 - 区间判断（向下兼容旧 API）
// ══════════════════════════════════════════════════════════════

bool distance_has_obstacle(void); // < MID
bool distance_is_near(void);      // < NEAR
bool distance_is_far(void);       // > FAR
bool distance_is_critical(void);  // < CRITICAL（新增）

/*******************************************************************
 * @brief   获取当前距离所处区间
 ******************************************************************/
DistZone_t distance_get_zone(void);

/*******************************************************************
 * @brief   区间字符串（调试/屏幕显示）
 ******************************************************************/
const char *distance_zone_str(void);

// ══════════════════════════════════════════════════════════════
// 函数声明 - 高级特性
// ══════════════════════════════════════════════════════════════

/*******************************************************************
 * @brief   检测是否正在快速接近障碍
 *          条件：最近 DIST_APPROACH_TICKS 个周期内距离减少超过阈值
 * @return  true = 距离在快速变小（即将碰撞预警）
 ******************************************************************/
bool distance_is_approaching_fast(void);

/*******************************************************************
 * @brief   注册临界距离回调
 *          当距离进入 CRITICAL 区间时自动触发
 *          典型用法：绑定 close_loop_emergency_stop 实现自动急停
 * @param   cb   回调函数，传入 NULL 取消注册
 ******************************************************************/
void distance_register_critical_cb(dist_critical_cb_t cb);

/*******************************************************************
 * @brief   获取滤波窗口内的最大/最小/均值
 *          用于了解数据稳定性，调试场景用
 ******************************************************************/
uint16_t distance_get_max(void);
uint16_t distance_get_min(void);
uint16_t distance_get_avg(void);

/*******************************************************************
 * @brief   获取连续读取失败次数（用于诊断硬件问题）
 ******************************************************************/
uint16_t distance_get_fail_count(void);

/*******************************************************************
 * @brief   传感器是否在线（基于失败次数判断）
 * @return  false = 连续失败超过阈值，可能未连接或损坏
 ******************************************************************/
bool distance_is_online(void);

#endif // DISTANCE_SENSOR_H