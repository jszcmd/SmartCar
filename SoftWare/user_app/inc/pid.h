/*********************************************************************************************************************
 * 文件名称：pid.h
 * 功能描述：通用 PID 控制器（增量式 + 位置式）
 *
 * ─────────────────────────────────────────────────────────────────────
 * 核心思想：
 *   PID = Proportional(比例) + Integral(积分) + Derivative(微分)
 *   - P：当前误差越大，输出越大（响应快，但不够稳）
 *   - I：误差累积越久，输出越大（消除稳态误差，但易超调）
 *   - D：误差变化越快，输出越大（抑制超调，但放大噪声）
 *
 * 两种模式选择：
 *   增量式（PID_IncCtrl）：
 *     - 输出"变化量"（Δ控制量），需累加才是最终值
 *     - 适合电机速度控制（PWM 增减量）
 *     - 切换/复位平滑，无积分饱和遗留
 *   位置式（PID_PosCtrl）：
 *     - 输出"绝对值"，直接当作控制量
 *     - 适合舵机转向（直接对应角度）
 *     - 需要 imax 限幅防止积分饱和
 *
 * 高级特性：
 *   1. 输出限幅可配置（out_min / out_max），不再写死 ±2000
 *   2. 积分分离（i_separate_thresh）：误差大于阈值时停止积分，避免大幅超调
 *   3. 微分滤波（d_filter_alpha）：低通滤波微分项，降低高频噪声放大问题
 *   4. 提供 PID_Reset() 接口，紧急停车/重启时彻底清空状态
 *
 * 调用顺序：
 *   1. PID_Init(&pid, Kp, Ki, Kd)        → 初始化
 *   2. PID_SetOutputLimit(&pid, min, max) → （可选）配置输出限幅
 *   3. PID_SetISeparate(&pid, thresh)     → （可选）配置积分分离
 *   4. 周期调用 PID_IncCtrl 或 PID_PosCtrl
 *   5. PID_Reset(&pid)                    → 停车/紧急停止时调用
 ********************************************************************************************************************/

#ifndef __PID_H
#define __PID_H

// ══════════════════════════════════════════════════════════════
// PID 控制器结构体
// ══════════════════════════════════════════════════════════════
typedef struct
{
    // ── 三个核心参数 ──────────────────────────────────────────
    float kp; // 比例系数
    float ki; // 积分系数
    float kd; // 微分系数

    // ── 限幅配置 ──────────────────────────────────────────────
    float imax;    // 积分项限幅（位置式专用，0=不限制）
    float out_max; // 输出最大值
    float out_min; // 输出最小值

    // ── 高级特性 ──────────────────────────────────────────────
    float i_separate_thresh; // 积分分离阈值：|误差|>该值时不积分（0=禁用）
    float d_filter_alpha;    // 微分滤波系数（0~1，0=不滤波，0.7=强滤波）

    // ── 中间过程量（调试可观察）───────────────────────────────
    float out_p; // P 分量输出
    float out_i; // I 分量输出
    float out_d; // D 分量输出
    float out;   // PID 总输出

    // ── 历史状态（算法自维护，外部不要改）─────────────────────
    float integrator;      // 积分累积值（位置式用）
    float last_error;      // 上一次误差
    float last_derivative; // 上一次微分值（用于微分先行/二阶差分）
    float filtered_d;      // 微分项滤波后的值
} PID_TypeDef;

// ══════════════════════════════════════════════════════════════
// 全局实例（左右轮速度 PID 控制器）
// ══════════════════════════════════════════════════════════════
extern PID_TypeDef LSpeed_PID;
extern PID_TypeDef RSpeed_PID;

// ══════════════════════════════════════════════════════════════
// 函数声明
// ══════════════════════════════════════════════════════════════

/*******************************************************************
 * @brief   PID 初始化
 *          清空所有历史状态，设置默认输出限幅 ±10000（足够大）
 * @param   pid     PID 结构体指针
 * @param   Kp/Ki/Kd  三个参数
 * @note    默认参数：i_separate=0(禁用), d_filter=0(不滤波), imax=0(不限制)
 *          需要时通过 PID_SetXXX() 函数单独配置
 ******************************************************************/
void PID_Init(PID_TypeDef *pid, float Kp, float Ki, float Kd);

/*******************************************************************
 * @brief   PID 状态重置
 *          清零积分、历史误差、累积输出等所有运行时状态
 *          但保留 Kp/Ki/Kd 等配置参数
 * @note    紧急停车/重启控制时必调，否则积分残值会让电机突然冲转
 ******************************************************************/
void PID_Reset(PID_TypeDef *pid);

/*******************************************************************
 * @brief   配置输出限幅
 * @param   out_min   输出下限（如电机控制可设为 0 防倒车，舵机为 -3000）
 * @param   out_max   输出上限
 ******************************************************************/
void PID_SetOutputLimit(PID_TypeDef *pid, float out_min, float out_max);

/*******************************************************************
 * @brief   配置积分分离阈值
 *          |误差| > thresh 时停止积分累积（防止大误差导致积分爆炸）
 * @param   thresh   阈值（设 0 禁用此功能）
 ******************************************************************/
void PID_SetISeparate(PID_TypeDef *pid, float thresh);

/*******************************************************************
 * @brief   配置微分项低通滤波系数
 * @param   alpha    滤波强度 0~1
 *                   0   = 不滤波（响应快但噪声大）
 *                   0.5 = 中等
 *                   0.8 = 强滤波（噪声小但响应慢）
 ******************************************************************/
void PID_SetDFilter(PID_TypeDef *pid, float alpha);

/*******************************************************************
 * @brief   增量式 PID 控制
 *          公式：Δout = Kp*(e-e₁) + Ki*e + Kd*((e-e₁) - (e₁-e₂))
 *          内部累加 Δout 到 pid->out 上，并做限幅
 * @param   error   当前误差 = 目标值 - 实际值
 * @return  累加后的总输出（已限幅）
 ******************************************************************/
float PID_IncCtrl(PID_TypeDef *pid, float error);

/*******************************************************************
 * @brief   位置式 PID 控制
 *          公式：out = Kp*e + Ki*∑e + Kd*(e-e₁)
 * @param   error   当前误差 = 目标值 - 实际值
 * @return  位置式输出（已限幅）
 ******************************************************************/
float PID_PosCtrl(PID_TypeDef *pid, float error);

#endif // __PID_H