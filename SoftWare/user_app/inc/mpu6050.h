/*********************************************************************************************************************
 * 文件名称：mpu6050.h
 * 功能描述：陀螺仪模块（MPU6050）头文件
 *
 * ─────────────────────────────────────────────────────────────────────
 * 数据处理流程：
 *
 *   硬件原始 ADC 值（int16_t）
 *           │
 *           ├──► ax/ay/az/gx/gy/gz   ← 原始值（向下兼容）
 *           │
 *           ▼
 *   减去零偏（boot 时校准）
 *           │
 *           ▼
 *   单位换算：
 *      加速度： raw / 16384.0 → g       (±2g 量程下)
 *      角速度： raw / 131.0   → °/s     (±250°/s 量程下)
 *           │
 *           ├──► ax_g / ay_g / az_g    ← 浮点 g 值
 *           ├──► gx_dps / gy_dps / gz_dps ← °/s
 *           │
 *           ▼
 *   姿态解算：
 *     pitch / roll  ← 互补滤波（加速度计算 + 陀螺积分）
 *     yaw           ← 陀螺积分（无磁力计辅助，会缓慢漂移）
 *
 * ─────────────────────────────────────────────────────────────────────
 * 校准流程：
 *   1. 上电后 mpu6050_loop() 第一次调用进入校准模式
 *   2. 此时小车必须静止平放
 *   3. 连续采样 200 次求平均，得到零偏值
 *   4. 校准完成后 g_mpu_calibrated = true
 *   5. 后续每次采样减去零偏 → 静止时输出近似 0
 *
 * ─────────────────────────────────────────────────────────────────────
 * 重要说明：
 *   - 互补滤波系数 alpha = 0.98
 *     pitch_new = 0.98·(pitch_old + gyro·dt) + 0.02·acc_pitch
 *     陀螺仪短期准（高频用），加速度计长期准（低频用），互补
 *
 *   - yaw 没有外部参考（无磁力计/GPS），纯陀螺积分会漂移
 *     适合短时间内的转角检测（如旋转 90°），不适合长期航向保持
 *
 *   - dt（积分步长）由调用频率决定，定时器周期 20ms → dt = 0.02s
 ********************************************************************************************************************/

#ifndef MPU6050_H
#define MPU6050_H

#include <lq_i2c_mpu6050.hpp>
#include <stdint.h>

// ══════════════════════════════════════════════════════════════
// 配置宏
// ══════════════════════════════════════════════════════════════

// 单位换算系数（基于默认量程 ±2g 和 ±250°/s）
// 如果库内部配置了不同量程，请修改这两个值
#define MPU_ACC_LSB_PER_G 16384.0f  // ±2g 量程：1g = 16384 LSB
#define MPU_GYRO_LSB_PER_DPS 131.0f // ±250°/s 量程：1°/s = 131 LSB

// 零偏校准采样次数（越多越准，但启动时间越长）
#define MPU_CALIB_SAMPLES 200

// 互补滤波系数（0~1，越接近 1 陀螺权重越大、响应越快但抗噪差）
#define MPU_COMP_ALPHA 0.98f

// 调用周期（与定时器配置一致，单位秒）
// 在 WW_TimerTasks 里 mpu6050_loop 是 20ms 一次
#define MPU_DT 0.02f

// 故障次数阈值
#define MPU_FAIL_THRESHOLD 50

// ══════════════════════════════════════════════════════════════
// 全局变量声明
// ══════════════════════════════════════════════════════════════

// ── 原始 ADC 值（向下兼容旧代码）─────────────────────────────
extern int16_t ax, ay, az;
extern int16_t gx, gy, gz;

// ── 物理量（单位换算后）──────────────────────────────────────
extern float ax_g, ay_g, az_g;       // 加速度（g）
extern float gx_dps, gy_dps, gz_dps; // 角速度（°/s）

// ── 零偏校准值（校准完成后填充）──────────────────────────────
extern int16_t ax_offset, ay_offset, az_offset;
extern int16_t gx_offset, gy_offset, gz_offset;

// ── 姿态角（°）──────────────────────────────────────────────
extern float g_pitch; // 俯仰角（前倾后仰）
extern float g_roll;  // 横滚角（左右倾斜）
extern float g_yaw;   // 航向角（左右转向）

// ── 状态标志 ─────────────────────────────────────────────────
extern bool g_mpu_online;     // 设备是否在线
extern bool g_mpu_calibrated; // 是否已完成零偏校准

// ══════════════════════════════════════════════════════════════
// 函数声明
// ══════════════════════════════════════════════════════════════

/*******************************************************************
 * @brief   陀螺仪初始化 + 零偏校准
 *          调用时小车必须静止平放
 *          内部采样 MPU_CALIB_SAMPLES 次取平均，作为零偏值
 *          可选：未调用此函数，第一次 mpu6050_loop() 会自动尝试校准
 ******************************************************************/
void mpu6050_init(void);

/*******************************************************************
 * @brief   陀螺仪任务函数（定时器周期调用）
 *          - 读取原始数据
 *          - 减零偏
 *          - 单位换算
 *          - 姿态解算（互补滤波）
 *          - 故障检测
 *
 *          典型周期：20ms（在 WW_TimerTasks 里配置）
 ******************************************************************/
void mpu6050_loop(void);

/*******************************************************************
 * @brief   清零航向角 yaw（重置参考方向）
 *          典型场景：小车出发前调用一次，把当前朝向定为 0°
 ******************************************************************/
void mpu6050_reset_yaw(void);

/*******************************************************************
 * @brief   清零所有姿态角（pitch/roll/yaw）
 *          紧急情况或重新校准后调用
 ******************************************************************/
void mpu6050_reset_attitude(void);

/*******************************************************************
 * @brief   重新校准零偏（小车需静止）
 *          运行中如果发现零偏漂移，可手动触发重新校准
 ******************************************************************/
void mpu6050_recalibrate(void);

/*******************************************************************
 * @brief   打印调试信息
 ******************************************************************/
void mpu6050_print(void);

/*******************************************************************
 * @brief   预留扩展接口（保留向下兼容）
 ******************************************************************/
void mpu6050_update(float ax, float ay, float az,
                    float gx, float gy, float gz);

#endif // MPU6050_H