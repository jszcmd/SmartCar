/*********************************************************************************************************************
 * 文件名称：encoder.h
 * 功能描述：编码器模块头文件
 *
 * ─────────────────────────────────────────────────────────────────────
 * 数据流向：
 *
 *   硬件原始值（float, 带符号）
 *           │
 *           ├──→ encoder_L/R_signed   ← 保留方向，新增接口供姿态/里程使用
 *           │
 *           └──→ 取绝对值
 *                   │
 *                   ├──→ encoder_L/R_oringinal   ← 原始绝对值
 *                   │
 *                   ├──→ 一阶低通滤波
 *                   │       │
 *                   │       └──→ encoder_L/R_final  ← 业务主用接口（向下兼容）
 *                   │
 *                   └──→ 滑动窗口滤波（可选，更平滑）
 *                           │
 *                           └──→ encoder_L/R_smooth ← 慢速场景使用
 *
 *   累积计数：
 *   每周期把当前速度累加到 encoder_L/R_total，可估算行驶里程
 *
 * ─────────────────────────────────────────────────────────────────────
 * 滤波方法对比：
 *   一阶低通（默认）：响应快，对突变敏感，适合速度闭环
 *   滑动窗口：响应慢但更平滑，适合距离估算或慢速精确控制
 *
 * ─────────────────────────────────────────────────────────────────────
 * 修复说明：
 *   原代码 encoderL_100 用 uint16_t，最大 65535
 *   假设原始速度 > 655.35 时会溢出，已改为 uint32_t 保险
 ********************************************************************************************************************/

#ifndef ENCODER_H
#define ENCODER_H

#include "lq_reg_pwm_encoder.hpp"
#include <atomic>
#include <stdint.h>

// ══════════════════════════════════════════════════════════════
// 配置宏（根据小车参数调整）
// ══════════════════════════════════════════════════════════════

// 滑动窗口滤波长度（越大越平滑，但延迟越大）
// 设 1 等于禁用窗口滤波
#define ENCODER_SMOOTH_WINDOW 5

// 一阶低通滤波系数（0~10，新值占比 = 系数/10）
// 默认 1 = 新值占 10%（同原版本）
#define ENCODER_LPF_ALPHA 1

// 物理参数（用于速度/里程换算，可选功能）
// 假设：编码器一次读取代表一定脉冲数，乘以系数即可换算实际距离
// 这里默认 1.0，意思是不换算（用户根据自己的车实测一次写死）
#define ENCODER_MM_PER_UNIT 1.0f // 每个编码器单位对应多少 mm

// ══════════════════════════════════════════════════════════════
// 外部变量声明
// ══════════════════════════════════════════════════════════════

// 系统运行标志（main.cpp 定义）
extern std::atomic<bool> ls_system_running;

// ── 原始值（绝对值化后，向下兼容旧代码）──────────────────────
extern float encoderL_oringinal;
extern float encoderR_oringinal;

// ── 业务主用接口（一阶低通滤波后的绝对值，向下兼容）─────────
extern float encoder_L_final;
extern float encoder_R_final;

// ── 新增：带符号速度值（保留方向信息）────────────────────────
extern float encoder_L_signed;
extern float encoder_R_signed;

// ── 新增：滑动窗口滤波后的速度（更平滑）──────────────────────
extern float encoder_L_smooth;
extern float encoder_R_smooth;

// ── 新增：累积计数器（用于里程估算）──────────────────────────
// 注意：是 float 累加，长时间运行会有累积误差
// 需要精确里程时建议用 int64_t 计数硬件中断脉冲，本字段仅作估算
extern float encoder_L_total;
extern float encoder_R_total;

// ══════════════════════════════════════════════════════════════
// 函数声明
// ══════════════════════════════════════════════════════════════

/*******************************************************************
 * @brief   编码器初始化
 *          分配两个编码器实例（左 = 4号PWM，右 = 1号PWM）
 *          在 main() 里调用一次
 ******************************************************************/
void encoder_Init(void);

/*******************************************************************
 * @brief   编码器数据更新（定时器周期调用）
 *          完整流程：
 *            1) 读取硬件原始值（含正负方向）
 *            2) 更新带符号值 encoder_L/R_signed
 *            3) 取绝对值
 *            4) 一阶低通滤波 → encoder_L/R_final
 *            5) 滑动窗口滤波 → encoder_L/R_smooth
 *            6) 累加到 encoder_L/R_total
 *
 *          典型周期：5ms
 ******************************************************************/
void encoder_update(void);

/*******************************************************************
 * @brief   清空所有编码器状态
 *          清零滤波历史、累计里程、当前速度等
 *          紧急停车/重启控制时调用，避免滤波残值影响新一轮控制
 ******************************************************************/
void encoder_reset(void);

/*******************************************************************
 * @brief   清零累积里程（仅清里程，不影响滤波）
 *          需要重新计算"从某个起点行驶了多远"时调用
 ******************************************************************/
void encoder_reset_total(void);

/*******************************************************************
 * @brief   获取累积行驶距离（mm，左轮）
 *          基于 encoder_L_total × ENCODER_MM_PER_UNIT 换算
 *          注意：精度依赖 ENCODER_MM_PER_UNIT 标定值
 * @return  距离（mm）
 ******************************************************************/
float encoder_get_left_distance_mm(void);

/*******************************************************************
 * @brief   获取累积行驶距离（mm，右轮）
 ******************************************************************/
float encoder_get_right_distance_mm(void);

/*******************************************************************
 * @brief   获取双轮平均距离（mm，估算车体行驶距离）
 ******************************************************************/
float encoder_get_avg_distance_mm(void);

/*******************************************************************
 * @brief   打印编码器调试信息
 ******************************************************************/
void encoder_print(void);

#endif // ENCODER_H